#!/usr/bin/env node
/**
 * Parse docs/2026Jobs.txt into SQL inserts for:
 *   - job_number_blocks  (the reserved ranges per customer)
 *   - crew_jobs          (the actual jobs that already have numbers assigned)
 *
 * Usage (from project root):
 *   node scripts/parse-2026-jobs.js [input-file] [--out scripts/seed-2026-jobs.sql]
 *
 * If --out is omitted, SQL is written to stdout.
 */

const fs = require("fs");
const path = require("path");

const DEFAULT_INPUT = "/Users/alechermesmeyer/Desktop/SWF/final/swnext/docs/2026Jobs.txt";

function escapeSqlString(value) {
  if (value === null || value === undefined) return "NULL";
  const s = String(value);
  if (s.length === 0) return "''";
  return `'${s.replace(/'/g, "''")}'`;
}

function numOrNull(value) {
  return value === null || value === undefined || value === "" ? "NULL" : String(value);
}

function parseBlockLine(line) {
  // "26/0120 - 26/0199   Archer Western"
  const m = line.match(/^(\d{2})[\/\-](\d{4})\s*-\s*\d{2}[\/\-](\d{4})\s+(.+)$/);
  if (!m) return null;
  return {
    year_prefix: m[1],
    start_num: parseInt(m[2], 10),
    end_num: parseInt(m[3], 10),
    owner: m[4].trim(),
  };
}

function parseJobLine(line) {
  // "26/0120   Mansfield Bud Ervin WTP" (tolerate stray leading whitespace)
  const m = line.match(/^(\d{2})[\/\-](\d{4})\s+(.+)$/);
  if (!m) return null;
  return {
    job_number: `${m[1]}/${m[2]}`,  // normalize dash to slash
    job_name: m[3].trim(),
  };
}

function deriveMiscLetter(blockOwner) {
  // "Misc. A's", "Misc A's", "Misc X & Y'S"
  const m = blockOwner.match(/^Misc\.?\s+([A-Z])/i);
  return m ? m[1].toUpperCase() : null;
}

function parse(inputPath) {
  const raw = fs.readFileSync(inputPath, "utf8");
  const lines = raw.split(/\r?\n/);

  const blocks = [];
  const jobs = [];

  let currentBlock = null;
  let currentSubContractor = null;
  let warnings = [];

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    const trimmed = line.trim();

    if (!trimmed) {
      currentSubContractor = null;  // blank line resets the sub-contractor
      continue;
    }

    // Block line?
    const blockParsed = parseBlockLine(trimmed);
    if (blockParsed) {
      const miscLetter = deriveMiscLetter(blockParsed.owner);
      currentBlock = {
        year_prefix: blockParsed.year_prefix,
        start_num: blockParsed.start_num,
        end_num: blockParsed.end_num,
        block_owner: blockParsed.owner,
        is_misc: Boolean(miscLetter),
        misc_letter: miscLetter,
      };
      blocks.push(currentBlock);
      currentSubContractor = null;
      continue;
    }

    // Job line?
    const jobParsed = parseJobLine(trimmed);
    if (jobParsed) {
      let hiringContractor = "";
      if (currentBlock) {
        hiringContractor = currentBlock.is_misc
          ? (currentSubContractor || currentBlock.block_owner)
          : currentBlock.block_owner;
      }
      // Sanity check: job number should fall within current block's range
      const [, , seqStr] = jobParsed.job_number.match(/(\d{2})\/(\d{4})/) || [];
      const seq = parseInt(seqStr, 10);
      if (currentBlock && (seq < currentBlock.start_num || seq > currentBlock.end_num)) {
        warnings.push(
          `Line ${i + 1}: job ${jobParsed.job_number} "${jobParsed.job_name}" outside current block ${currentBlock.block_owner} (${currentBlock.start_num}-${currentBlock.end_num}). Importing as-is.`
        );
      }
      jobs.push({
        job_number: jobParsed.job_number,
        job_name: jobParsed.job_name,
        hiring_contractor: hiringContractor,
        block_owner: currentBlock?.block_owner || null,
      });
      continue;
    }

    // Otherwise: treat as a sub-contractor name within a Misc block.
    // (In dedicated blocks these lines are rare/noise and we just ignore.)
    if (currentBlock?.is_misc) {
      currentSubContractor = trimmed;
    }
  }

  return { blocks, jobs, warnings };
}

function renderSql({ blocks, jobs }, { inputPath, isActiveDefault, jobStatusDefault }) {
  const out = [];
  out.push(`-- Auto-generated by scripts/parse-2026-jobs.js from ${path.basename(inputPath)}`);
  out.push("-- Run AFTER scripts/add-job-number-blocks.sql has created the blocks table.");
  out.push("-- Idempotent: ON CONFLICT on blocks; WHERE NOT EXISTS on jobs.");
  out.push(`-- Defaults: is_active=${isActiveDefault}, job_status='${jobStatusDefault}'`);
  out.push("");
  out.push("-- ── Job-number blocks ──────────────────────────────────────────");
  out.push("INSERT INTO public.job_number_blocks (year_prefix, start_num, end_num, block_owner, is_misc, misc_letter)");
  out.push("VALUES");

  const blockRows = blocks.map((b) =>
    `  (${escapeSqlString(b.year_prefix)}, ${b.start_num}, ${b.end_num}, ${escapeSqlString(b.block_owner)}, ${b.is_misc}, ${escapeSqlString(b.misc_letter)})`
  );
  out.push(blockRows.join(",\n"));
  out.push("ON CONFLICT (year_prefix, start_num, end_num) DO UPDATE");
  out.push("  SET block_owner = EXCLUDED.block_owner,");
  out.push("      is_misc = EXCLUDED.is_misc,");
  out.push("      misc_letter = EXCLUDED.misc_letter;");
  out.push("");

  if (jobs.length > 0) {
    out.push("-- ── Jobs ──────────────────────────────────────────────────────");
    out.push("-- Inserts any job_number not already present in crew_jobs.");
    out.push("INSERT INTO public.crew_jobs (job_number, job_name, hiring_contractor, is_active, job_status)");
    out.push("SELECT * FROM (VALUES");
    const jobRows = jobs.map((j) =>
      `  (${escapeSqlString(j.job_number)}, ${escapeSqlString(j.job_name)}, ${escapeSqlString(j.hiring_contractor || null)}, ${isActiveDefault}, ${escapeSqlString(jobStatusDefault)})`
    );
    out.push(jobRows.join(",\n"));
    out.push(") AS v(job_number, job_name, hiring_contractor, is_active, job_status)");
    out.push("WHERE NOT EXISTS (");
    out.push("  SELECT 1 FROM public.crew_jobs existing");
    out.push("  WHERE existing.job_number = v.job_number");
    out.push(");");
  }
  out.push("");
  return out.join("\n");
}

function main() {
  const argv = process.argv.slice(2);
  let inputPath = DEFAULT_INPUT;
  let outPath = null;
  let isActiveDefault = true;
  let jobStatusDefault = "active";

  for (let i = 0; i < argv.length; i += 1) {
    if (argv[i] === "--out" && argv[i + 1]) {
      outPath = argv[i + 1];
      i += 1;
    } else if (argv[i] === "--inactive") {
      isActiveDefault = false;
      jobStatusDefault = "completed";
    } else if (argv[i] === "--status" && argv[i + 1]) {
      jobStatusDefault = argv[i + 1];
      i += 1;
    } else if (!argv[i].startsWith("--")) {
      inputPath = argv[i];
    }
  }

  if (!fs.existsSync(inputPath)) {
    console.error(`ERROR: input file not found: ${inputPath}`);
    process.exit(1);
  }

  const { blocks, jobs, warnings } = parse(inputPath);
  const sql = renderSql({ blocks, jobs }, { inputPath, isActiveDefault, jobStatusDefault });

  if (outPath) {
    const abs = path.isAbsolute(outPath) ? outPath : path.resolve(process.cwd(), outPath);
    fs.writeFileSync(abs, sql, "utf8");
    console.error(`✔ Wrote ${blocks.length} blocks and ${jobs.length} jobs to ${abs}`);
    console.error(`  is_active=${isActiveDefault}, job_status='${jobStatusDefault}'`);
  } else {
    process.stdout.write(sql);
  }
  if (warnings.length > 0) {
    console.error(`\n${warnings.length} warning(s):`);
    warnings.forEach((w) => console.error("  • " + w));
  }
}

if (require.main === module) {
  main();
}
